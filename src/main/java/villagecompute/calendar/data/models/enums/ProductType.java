package villagecompute.calendar.data.models.enums;

/**
 * Product types available for purchase.
 * Determines the physical product format and pricing.
 */
public enum ProductType {
    /**
     * Standard 12-month wall calendar (11x17 inches).
     */
    WALL_CALENDAR,

    /**
     * Desk calendar with stand (8x10 inches).
     */
    DESK_CALENDAR,

    /**
     * Poster format (18x24 inches, single page).
     */
    POSTER
}
